# Variant 1


def is_correct_bracket0(text: str):
    counter = 0
    for c in text:
        if c == '(':
            counter += 1
        else:
            counter -= 1
        if counter < 0:
            return False
    return not counter

# Variant 2


def is_correct_bracket(text: str):
    string = text
    while "()" in string:
        string = string.replace('()', '')
    return not string


